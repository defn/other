;; Exceptions for app name -> CUE schema key mapping.
;; Default rule: replace "-" with "_".
(def app-cue-key-overrides
  {"linkerd-control-plane" "linkerd"
   "tailscale-operator"    "tailscale"})


(defn app->cue-key
  "Map a catalog app name to its CUE schema key in versions.cue."
  [app-name]
  (or (app-cue-key-overrides app-name)
      (str/replace app-name "-" "_")))


(defn strip-v
  "Remove leading 'v' prefix from a version string."
  [s]
  (if (and s (str/starts-with? s "v"))
    (subs s 1)
    s))


(defn patch-cue-tool-field
  "Replace a field's quoted value within a tool's #ToolVersion block.
   Anchored to the tool header so it cannot cross block boundaries."
  [content tool-name field-name new-value]
  (let [tool-pat (if (str/includes? tool-name "-")
                   (str "\"" (java.util.regex.Pattern/quote tool-name) "\"")
                   (java.util.regex.Pattern/quote tool-name))
        field-pat (java.util.regex.Pattern/quote field-name)
        pattern  (re-pattern
                   (str "(\t" tool-pat ":\\s*#ToolVersion\\s*&\\s*\\{[^}]*?"
                        field-pat ":\\s+\")[^\"]*(\")"))
        replaced (str/replace content pattern (str "$1" new-value "$2"))]
    replaced))


(defn helm-show-chart
  "Run helm show chart, return {:chart-version :app-version} or nil."
  [chart-name chart-repo]
  (try
    (let [oci?      (str/starts-with? chart-repo "oci://")
          chart-ref (if oci? (str chart-repo "/" chart-name) chart-name)
          args      (if oci?
                      ["helm" "show" "chart" chart-ref]
                      ["helm" "show" "chart" chart-ref "--repo" chart-repo])
          output    (str/trim (:out (apply sh!!? args)))]
      (when-not (str/blank? output)
        (let [cv (second (re-find #"(?m)^version:\s*(.+)" output))
              av (second (re-find #"(?m)^appVersion:\s*(.+)" output))]
          (when cv
            {:chart-version (str/trim cv)
             :app-version   (str/trim (or av ""))}))))
    (catch Exception _ nil)))


(defn discover-app-updates
  "Query all catalog apps for available chart upgrades.
   Returns seq of maps with :app-name, :cue-key, :chart-name, :chart-repo,
   :current-chart-ver, :current-app-ver, :latest-chart-ver, :latest-app-ver,
   :upgrade?"
  []
  ;; Helm-app instances live in the library tenant after the kernel/tenant
  ;; decoupling (kernel/catalog/apps.cue is schema-only). Only library carries
  ;; charts -- defn-tenant apps are all `raw` (no chart_name/chart_repo) and
  ;; would be filtered out anyway.
  (let [apps-json    (run-tool-quiet "cue" "export" "-e" "apps" "--out" "json"
                                     "./tenant/library/catalog")
        apps         (parse-json apps-json)
        versions-json (run-tool-quiet "cue" "export" "-e" "versions" "--out" "json"
                                      "./kernel/schema")
        versions     (parse-json versions-json)]
    (->> apps
         (pmap (fn [[app-name app-info]]
                 (let [chart-name (get app-info "chart_name")
                       chart-repo (get app-info "chart_repo")
                       app-path   (get app-info "path")]
                   (when (and chart-name chart-repo)
                     (let [cue-key     (app->cue-key app-name)
                           current-cv  (get-in versions [cue-key "chart_version"])
                           current-av  (get-in versions [cue-key "version"])
                           latest      (helm-show-chart chart-name chart-repo)]
                       (when latest
                         (let [lcv (:chart-version latest)
                               lav (:app-version latest)]
                           {:app-name          app-name
                            :app-path          app-path
                            :cue-key           cue-key
                            :chart-name        chart-name
                            :chart-repo        chart-repo
                            :current-chart-ver current-cv
                            :current-app-ver   current-av
                            :latest-chart-ver  (strip-v lcv)
                            :latest-app-ver    (strip-v lav)
                            :upgrade?          (and (not= (strip-v lcv) current-cv)
                                                    (not (prerelease? lcv)))})))))))
         (remove nil?)
         (sort-by :app-name))))


(defn extract-images
  "Extract all container image references from a rendered manifest file."
  [gen-app-path]
  (when (fs/exists? gen-app-path)
    (->> (str/split-lines (slurp (str gen-app-path)))
         (keep #(second (re-find #"image:\s+\"([^\"]+)\"" %)))
         set)))


(defn pre-validate-chart
  "Dry-run kustomize build with the new chart to catch template errors
   (e.g. missing required values) before committing to the upgrade.
   Returns nil on success, error string on failure."
  [app-path chart-tgz chart-name new-chart-ver]
  (let [tmp (str (create-temp-dir {:prefix "validate-"}))]
    (try
      ;; Copy kustomization.yaml with updated chart version
      (copy-file (str app-path "/kustomization.yaml") (str tmp "/kustomization.yaml"))
      (let [kust (slurp (str tmp "/kustomization.yaml"))
            name-pat (java.util.regex.Pattern/quote chart-name)
            kust (str/replace kust
                              (re-pattern (str "(?m)(name:\\s*" name-pat "\\s*\n\\s*version:\\s*)[^\n]+"))
                              (str "$1" new-chart-ver))]
        (spit (str tmp "/kustomization.yaml") kust))
      ;; Extract chart into charts/ subdir
      (fs/create-dirs (str tmp "/charts"))
      (sh! "tar" "xzf" (str chart-tgz) "-C" (str tmp "/charts"))
      ;; Run kustomize build
      (let [result (sh!!? "kustomize" "build" "--enable-helm" tmp)]
        (if (zero? (:exit result))
          nil
          (str/trim (:err result))))
      (finally
        (delete-tree tmp)))))


(defn download-chart!
  "Download a chart to tmp-dir. Returns path to the .tgz or throws."
  [chart-name chart-repo chart-ver tmp-dir]
  (let [oci?      (str/starts-with? chart-repo "oci://")
        chart-ref (if oci? (str chart-repo "/" chart-name) chart-name)
        pull-args (if oci?
                    ["helm" "pull" chart-ref "--version" chart-ver "-d" tmp-dir]
                    ["helm" "pull" chart-ref "--repo" chart-repo
                     "--version" chart-ver "-d" tmp-dir])]
    (apply sh!! pull-args)
    (first (fs/glob tmp-dir "*.tgz"))))


(defn apply-chart-update!
  "Download new chart, validate, update versions.cue, kustomization.yaml,
   swap tarballs. Returns result map with :status (:ok or :skipped),
   :old-manifest, :new-manifest, :old-images."
  [{:keys [app-name app-path cue-key chart-name chart-repo
           current-chart-ver current-app-ver
           latest-chart-ver latest-app-ver]}]
  (let [kust-path (str app-path "/kustomization.yaml")
        tmp-dir  (str (create-temp-dir {:prefix "upgrade-app-"}))]

    ;; 1. Save old manifest and extract old images for later comparison
    (let [old-manifest (str app-path "/gen-app.cue")
          old-images   (extract-images old-manifest)]
      (when (fs/exists? old-manifest)
        (copy-file old-manifest (str tmp-dir "/gen-app-old.cue")))

      ;; 2. Download new chart
      (let [new-tgz (download-chart! chart-name chart-repo latest-chart-ver tmp-dir)]

        ;; 3. Pre-validate: dry-run kustomize build with new chart
        (let [err (pre-validate-chart app-path (str new-tgz) chart-name latest-chart-ver)]
          (when err
            (log-err (str app-name ": template validation failed -- skipping"))
            (log-err (str "  " (first (str/split-lines err))))
            (println (str "  Fix: add required values to " kust-path " then re-run"))
            (println)
            ;; Return skipped status so caller knows not to include in gen
            (throw (ex-info "validation-failed"
                            {:status     :skipped
                             :app-name   app-name
                             :error      err}))))

        ;; 4. Compute SHA256
        (let [sha256   (first (str/split (sh! "sha256sum" (str new-tgz)) #"\s+"))
              tgz-name (str (fs/file-name new-tgz))]

          ;; 5. Update versions.cue
          (let [cue-path "kernel/schema/versions.cue"
                content  (-> (slurp cue-path)
                             (patch-cue-tool-field cue-key "chart_version" latest-chart-ver)
                             (patch-cue-tool-field cue-key "chart_sha256" sha256)
                             (patch-cue-tool-field cue-key "version" latest-app-ver))]
            (spit cue-path content)
            (sh! "chmod" "644" cue-path))

          ;; 6. Update kustomization.yaml -- chart version + image tags
          (when (fs/exists? kust-path)
            (let [kust     (slurp kust-path)
                  name-pat (java.util.regex.Pattern/quote chart-name)
                  kust     (str/replace kust
                                        (re-pattern (str "(?m)(name:\\s*" name-pat
                                                         "\\s*\n\\s*version:\\s*)"
                                                         (java.util.regex.Pattern/quote current-chart-ver)))
                                        (str "$1" latest-chart-ver))
                  old-bare (strip-v current-app-ver)
                  new-bare (strip-v latest-app-ver)
                  kust     (if (and (not= old-bare new-bare)
                                    (not (str/blank? old-bare)))
                             (-> kust
                                 (str/replace
                                   (re-pattern (str "(?m)(newTag:\\s+)v"
                                                    (java.util.regex.Pattern/quote old-bare)
                                                    "\\s*$"))
                                   (str "$1v" new-bare))
                                 (str/replace
                                   (re-pattern (str "(?m)(newTag:\\s+)"
                                                    (java.util.regex.Pattern/quote old-bare)
                                                    "\\s*$"))
                                   (str "$1" new-bare)))
                             kust)]
              (spit kust-path kust)))

          ;; 7. (mirrors.cue bump happens in `defn hatch helmupgrade` via
          ;;     stamp.BumpMirrorTagsFromKustomization -- it reads the newTag
          ;;     values just written in step 6 and rewrites each matching
          ;;     mirror_images entry atomically (source/tag/digest). The CUE
          ;;     key-parity invariant in catalog/mirrors.cue guards against
          ;;     partial updates.)

          ;; 8. Swap tarballs
          (doseq [old-tgz (fs/glob app-path "*.tgz")]
            (fs/delete old-tgz))
          (copy-file (str new-tgz) (str app-path "/" tgz-name))))

      {:status      :ok
       :app-name    app-name
       :old-manifest (str tmp-dir "/gen-app-old.cue")
       :new-manifest (str app-path "/gen-app.cue")
       :old-images  old-images})))


(defn show-manifest-diff
  "Show deduplicated diff between old and new rendered manifests."
  [{:keys [app-name old-manifest new-manifest]}]
  (when (fs/exists? old-manifest)
    (let [result (sh!!? "diff" old-manifest new-manifest)]
      (if (zero? (:exit result))
        (log-ok (str app-name ": no manifest changes"))
        (let [out-lines  (str/split-lines (:out result))
              changed    (filter #(re-matches #"^[<>].*" %) out-lines)
              unique     (sort (set changed))]
          (println)
          (println (str "--- " app-name " (" (count changed) " changed lines) ---"))
          (doseq [line (take 30 unique)]
            (println (str "  " line)))
          (when (> (count unique) 30)
            (println (str "  ... and " (- (count unique) 30) " more"))))))))


(defn upgrade-app!
  "Discover and apply helm chart upgrades.
   With no args: dry-run showing available upgrades.
   With app names: upgrade those apps.
   With --all: upgrade everything available."
  [args]
  (println "=== upgrade app ===")
  (println)

  (log-ok "discovering available chart upgrades...")
  (println)

  (let [updates    (discover-app-updates)
        upgrade-all (some #{"--all"} args)
        app-filter  (when-not upgrade-all
                      (set (remove #(str/starts-with? % "-") args)))]

    ;; Show discovery results
    (doseq [{:keys [app-name current-chart-ver latest-chart-ver
                    current-app-ver latest-app-ver upgrade?]} updates]
      (let [selected (or upgrade-all
                         (and (seq app-filter) (app-filter app-name)))]
        (cond
          (not upgrade?)
          (log-ok (str app-name " " current-chart-ver
                       " (app " current-app-ver ") -- up to date"))

          (and upgrade? (not selected))
          (println (str "  AVAILABLE: " app-name " "
                        current-chart-ver " -> " latest-chart-ver
                        " (app " current-app-ver " -> " latest-app-ver ")"))

          :else
          (println (str "  UPGRADE:   " app-name " "
                        current-chart-ver " -> " latest-chart-ver
                        " (app " current-app-ver " -> " latest-app-ver ")")))))

    (println)

    ;; If dry-run (no args), stop here
    (when (empty? args)
      (log-ok "dry run -- pass app names or --all to upgrade")
      (exit 0))

    ;; Filter to apps that need upgrading and are selected
    (let [to-upgrade (filter (fn [{:keys [app-name upgrade?]}]
                               (and upgrade?
                                    (or upgrade-all
                                        (app-filter app-name))))
                             updates)]

      (when (empty? to-upgrade)
        (log-ok "nothing to upgrade")
        (exit 0))

      ;; Apply upgrades (with pre-validation)
      (log-ok (str "upgrading " (count to-upgrade) " chart(s)..."))
      (println)

      (let [results (reduce (fn [acc info]
                              (log-ok (str (:app-name info) ": downloading "
                                           (:chart-name info) " "
                                           (:latest-chart-ver info)))
                              (try
                                (conj acc (apply-chart-update! info))
                                (catch clojure.lang.ExceptionInfo e
                                  (if (= :skipped (:status (ex-data e)))
                                    acc
                                    (throw e)))))
                            [] to-upgrade)
            applied (filter #(= :ok (:status %)) results)]

        (when (empty? applied)
          (log-ok "no charts passed validation -- nothing to rebuild")
          (exit 0))

        ;; Hatch each upgraded app to equilibrium: gen + build + sync,
        ;; detect image source changes, fix app.cue + mirrors.cue,
        ;; re-hatch if needed, then validate with full pipeline.
        (println)
        (doseq [{:keys [app-name]} applied]
          (defn-bin! "hatch" "helmupgrade" app-name))
        (println)

        ;; Show diffs
        (log-ok "manifest changes:")
        (doseq [r applied]
          (show-manifest-diff r))

        (println)
        (log-ok (str "upgraded " (count applied) " chart(s). Review changes, then commit."))))))


;; ---------------------------------------------------------------------------
;; Main: dispatch subcommands
;; ---------------------------------------------------------------------------

(let [cmds (vec *command-line-args*)]
  (when (empty? cmds)
    (println "Usage: mise run upgrade <subcommand>...")
    (println)
    (println "Subcommands:")
    (println "  all                  Upgrade everything (go, bzlmod, mise)")
    (println "  app                  List available helm chart upgrades (dry run)")
    (println "  app NAME [NAME...]   Upgrade specific app charts")
    (println "  app --all            Upgrade all app charts")
    (println "  bzlmod               Upgrade Bazel module versions from BCR")
    (println "  go                   Upgrade Go module dependencies")
    (println "  mise                 Upgrade mise tools, sync constrained versions")
    (println)
    (println "Examples:")
    (println "  mise run upgrade all")
    (println "  mise run upgrade app")
    (println "  mise run upgrade app argocd traefik")
    (println "  mise run upgrade app -- --all")
    (println "  mise run upgrade mise")
    (println "  mise run upgrade bzlmod")
    (println "  mise run upgrade go")
    (exit 1))

  (if (= (first cmds) "app")
    (upgrade-app! (rest cmds))
    (doseq [cmd cmds]
      (case cmd
        "all"    (do (upgrade-go!) (println)
                     (upgrade-bzlmod!) (println)
                     (upgrade-mise!))
        "bzlmod" (upgrade-bzlmod!)
        "go"     (upgrade-go!)
        "mise"   (upgrade-mise!)
        (do
          (log-err (str "unknown subcommand: " cmd))
          (exit 1)))
      (println))))
