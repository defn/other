#!/usr/bin/env bbs
#MISE description= "Upgrade tool and module versions"


;; upgrade -- orchestrate version upgrades with subcommands.
;;
;; Usage:
;;   mise run upgrade mise              # upgrade mise tools, sync constrained versions
;;   mise run upgrade bzlmod            # upgrade Bazel module versions via BCR
;;   mise run upgrade go                # upgrade Go module dependencies
;;   mise run upgrade bzlmod mise go    # upgrade all (bzlmod first, then mise, then go)
;;
;; ## Subcommand: mise
;;
;; 1. Runs `mise upgrade --bump` to bump all mise-managed tools,
;;    excluding bzlmod-pinned tools (uv, python, node) whose versions
;;    are constrained by their Bazel modules
;; 2. Regenerates mise.toml from the (now-stale) versions.cue
;;    so that mise.toml reflects the bumped versions -- this is needed
;;    because `mise upgrade --bump` edits mise.toml directly
;; 3. Reads the bumped versions back from mise.toml and patches versions.cue
;;    to match (so CUE remains the source of truth)
;; 4. Runs sync-module-versions to clamp constrained tools (uv, python, node)
;;    back to the max version their Bazel modules support
;; 5. Runs `mise run gen` to regenerate all derived files
;; 6. Runs `mise install` to install any rolled-back versions
;;
;; ## Subcommand: go
;;
;; 1. Parses go.mod for direct dependencies (excludes indirect)
;; 2. Runs `go get -u` on each direct dependency individually so that
;;    transitive pseudo-version pins (e.g. k8s.io/api -> protobuf) don't
;;    collide with another direct's @upgrade target.
;; 3. Clamps k8s.io/* directs back down to whatever sigs.k8s.io/controller-runtime
;;    requires -- controller-runtime tags lag the k8s release cycle, so the
;;    independent @upgrade pass leaves k8s.io ahead of what controller-runtime
;;    can compile against.
;; 4. Runs `go mod tidy` to clean up go.sum (via hatch goupgrade)
;;
;; ## Subcommand: bzlmod
;;
;; 1. Queries the Bazel Central Registry (bcr.bazel.build) for the latest
;;    non-yanked, non-prerelease version of each Bazel module
;; 2. Updates versions.cue with the new module versions
;; 3. After bzlmod, you typically run `mise` subcommand too, because
;;    upgrading rules_uv/rules_python/rules_nodejs may change the
;;    constrained tool versions
;;
;; ## Version detection schemes (for agent maintenance)
;;
;; When a Bazel module changes its internal structure, the detection in
;; sync-module-versions.clj may break. Here's how each works:
;;
;; ### uv ← rules_uv
;; - File: uv/private/uv.lock.json (rules_multitool JSON lock)
;; - URL:  https://raw.githubusercontent.com/theoremlp/rules_uv/v{VER}/uv/private/uv.lock.json
;; - Parse: JSON → .uv.binaries[0].url → extract version from download URL
;;   e.g. ".../download/0.8.12/uv-aarch64-..." → "0.8.12"
;; - If rules_uv moves away from rules_multitool, this will break.
;;   Look for alternative version declarations in the repo.
;;
;; ### python ← rules_python
;; - File: python/versions.bzl (Starlark)
;; - URL:  https://raw.githubusercontent.com/bazelbuild/rules_python/{VER}/python/versions.bzl
;; - Parse: Regex for MINOR_MAPPING = { "3.14": "3.14.2", ... }
;;   Extracts stable (non-alpha/beta) patch versions, returns latest by semver.
;; - Fallback: If MINOR_MAPPING disappears, falls back to parsing
;;   TOOL_VERSIONS = { "3.14.2": { ... } } keys directly.
;; - Note: rules_python tags do NOT have a "v" prefix (just "1.9.0").
;;
;; ### node ← rules_nodejs
;; - File: nodejs/private/node_versions.bzl (Starlark, auto-generated)
;; - URL:  https://raw.githubusercontent.com/bazelbuild/rules_nodejs/v{VER}/nodejs/private/node_versions.bzl
;; - Parse: Regex for "{VERSION}-linux_arm64" entries.
;;   Filters to even-major (LTS) versions only, returns latest by semver.
;; - The file is generated by scripts/update-nodejs-versions.js and covers
;;   Node 8.x through 24.x. If rules_nodejs changes the dict format or
;;   moves the file, detection will break.
;;
;; ### crane ← rules_oci
;; - File: oci/private/versions.bzl (Starlark)
;; - URL:  https://raw.githubusercontent.com/bazel-contrib/rules_oci/v{VER}/oci/private/versions.bzl
;; - Parse: Regex for CRANE_VERSIONS = { "v0.18.0": { ... } } top-level keys.
;;   Returns the latest version key (with "v" prefix) by semver.
;; - If rules_oci changes the dict name or file location, detection will break.
;;
;; ### regctl ← rules_oci
;; - File: oci/private/versions.bzl (same file as crane)
;; - Parse: Regex for REGCTL_VERSIONS = { "v0.8.0": { ... } } top-level keys.
;;   Returns the latest version key (with "v" prefix) by semver.
;;
;; ### java ← rules_java
;; - File: java/repositories.bzl (Starlark)
;; - URL:  https://raw.githubusercontent.com/bazelbuild/rules_java/{VER}/java/repositories.bzl
;; - Parse: Regex for JDK version in Zulu download URLs:
;;   e.g. "zulu25.32.17-ca-jdk25.0.2-linux_x64.tar.gz" → "25.0.2"
;;   Returns "graalvm-community-{latest}" to match mise format.
;; - Note: rules_java tags do NOT have a "v" prefix (just "9.6.1").
;; - If rules_java changes to a different JDK vendor or URL pattern,
;;   detection will break. Look for alternative version declarations.
;;
;; ### helm ← helm/helm releases (latest GA)
;; - URL:  https://api.github.com/repos/helm/helm/releases/latest
;; - Parse: Regex for "tag_name": "vX.Y.Z" (strip leading v).
;; - Tracks latest helm release regardless of major version. History:
;;   this used to be pinned to whatever ArgoCD's hack/tool-versions.sh
;;   (helm3_version) shipped, because argocd-server embedded helm to
;;   render charts. Decoupled 2026-04-20 -- the platform renders via
;;   plain helm/kustomize now, so the argocd-pin is no longer relevant.
;;
;; ### kustomize ← kubernetes-sigs/kustomize releases
;; - URL:  https://api.github.com/repos/kubernetes-sigs/kustomize/releases
;; - Parse: Filter to "tag_name": "kustomize/vX.Y.Z"; pick highest semver.
;;   (The repo also tags api/v*, cmd/config/v*, kyaml/v*; we only want
;;   the kustomize/v* tags.)
;; - Same decoupling story as helm.
;;
;; ### k3s ← k3s v1.{minor} channel (minor from versions.cue)
;; - URL:  https://update.k3s.io/v1-release/channels/v1.{minor}
;; - Parse: Regex for vX.Y.Z in the redirect/response body.
;; - The minor family is anchored by the k3s version in versions.cue (not the
;;   upstream "stable" channel, which may lag behind). To move to a new k3s
;;   family (e.g. 1.36), update versions.cue k3s.version to any 1.36.x and
;;   sync-module-versions will pick up the latest 1.36 patch automatically.
;;
;; ### kubectl ← k3s
;; - kubectl version is set to match the k3s stable version directly.
;; - No external detection needed -- it mirrors whatever k3s resolves to.
;;
;; ### Bazel modules ← BCR
;; - URL:  https://bcr.bazel.build/modules/{NAME}/metadata.json
;; - Parse: JSON → .versions (list), .yanked_versions (map of version→reason)
;;   Filters out yanked versions and pre-release (contains "-rc", "-alpha", etc.).
;;   Returns the latest by semver-ish comparison.
;; - BCR metadata format is stable (part of the registry protocol).
;;   Unlikely to break unless a module is removed from BCR entirely.


(require '[defn :refer :all])


;; ---------------------------------------------------------------------------
;; Version helpers
;; ---------------------------------------------------------------------------

(defn parse-version-parts
  "Parse a version string into comparable parts.
   Strips non-numeric segments (like 'bcr') and returns a vector of integers.
   Handles semver (1.2.3), two-part (1.2), and BCR suffixes (34.0.bcr.1)."
  [v]
  (->> (str/split v #"\.")
       (filter #(re-matches #"\d+" %))
       (mapv parse-long)))


(defn version-compare
  "Compare two version strings. Pads shorter vectors with zeros so that
   length differences don't dominate (e.g. 7.1.0 > 6.0.0.1)."
  [a b]
  (let [pa  (parse-version-parts a)
        pb  (parse-version-parts b)
        len (max (count pa) (count pb))
        pad (fn [v] (into v (repeat (- len (count v)) 0)))]
    (compare (pad pa) (pad pb))))


(defn latest-version
  "Return the latest version from a collection."
  [versions]
  (last (sort version-compare versions)))


(defn prerelease?
  "True if the version looks like a pre-release (rc, alpha, beta, etc.)."
  [v]
  (boolean (re-find #"(?i)[-](rc|alpha|beta|dev|pre)" v)))


;; ---------------------------------------------------------------------------
;; BCR helpers
;; ---------------------------------------------------------------------------

(defn fetch-url
  "Fetch a URL, returning {:ok body} or {:err message}."
  [url]
  (try
    {:ok (slurp url)}
    (catch Exception e
      {:err (str "fetch failed: " (.getMessage e))})))


(defn bcr-latest-version
  "Query BCR for the latest non-yanked, non-prerelease version of a module."
  [module-name]
  (let [url    (str "https://bcr.bazel.build/modules/" module-name "/metadata.json")
        result (fetch-url url)]
    (if-let [body (:ok result)]
      (let [data     (parse-json body)
            versions (get data "versions")
            yanked   (set (keys (get data "yanked_versions")))
            usable   (->> versions
                          (remove yanked)
                          (remove prerelease?))]
        (if (seq usable)
          {:ok (latest-version usable)}
          {:err (str "cannot-be-determined: no usable versions for " module-name)}))
      {:err (str "cannot-be-determined: " (:err result))})))


;; ---------------------------------------------------------------------------
;; versions.cue patching (shared with sync-module-versions)
;; ---------------------------------------------------------------------------

(defn patch-cue-version
  "Replace the version string for a tool in versions.cue content.
   Anchored with tab to avoid partial name matches."
  [content tool-name new-version]
  (let [tool-pat (if (str/includes? tool-name "-")
                   (str "\"" (java.util.regex.Pattern/quote tool-name) "\"")
                   (java.util.regex.Pattern/quote tool-name))
        pattern  (re-pattern
                   (str "(\t" tool-pat ":\\s*#ToolVersion\\s*&\\s*\\{\\s*\n\\s*version:\\s*\")[^\"]*(\")"))
        replaced (str/replace content pattern (str "$1" new-version "$2"))]
    replaced))


;; ---------------------------------------------------------------------------
;; Subcommand: bzlmod
;; ---------------------------------------------------------------------------

;; Bazel module names are derived from MODULE.bazel at runtime: every
;; `bazel_dep(name = "X", ...)` line is a candidate for upgrade. Module names
;; in MODULE.bazel match CUE keys in versions.cue 1:1 (both use underscores),
;; so no further translation is needed.
;;
;; History: this was a hand-maintained list, which silently rotted as
;; modules were added/removed -- e.g. `toolchains_protoc` was removed from
;; MODULE.bazel but lingered in the list, producing a phantom
;; "UPDATE: toolchains_protoc -> 0.6.1" each run that patched nothing.
(defn read-bazel-modules
  "Parse MODULE.bazel and return the list of bazel_dep names."
  []
  (let [content (slurp "MODULE.bazel")
        pat     #"(?m)^bazel_dep\(\s*name\s*=\s*\"([^\"]+)\""]
    (->> (re-seq pat content)
         (map second)
         distinct
         vec)))


(defn upgrade-bzlmod!
  "Query BCR for latest versions of all Bazel modules and patch versions.cue."
  []
  (println "=== upgrade bzlmod ===")
  (println)

  (let [versions-json (run-tool-quiet "cue" "eval" "-e" "versions" "--out" "json"
                                      "./kernel/schema")
        versions      (parse-json versions-json)
        cue-path      "kernel/schema/versions.cue"
        original      (slurp cue-path)
        changed       (atom false)
        bazel-modules (read-bazel-modules)]

    ;; Query all modules in parallel (babashka pmap)
    (let [results (pmap (fn [mod-name]
                          (let [current (get-in versions [mod-name "version"])
                                latest  (bcr-latest-version mod-name)]
                            {:module  mod-name
                             :current current
                             :latest  latest}))
                        bazel-modules)]

      ;; Print results
      (doseq [{:keys [module current latest]} (sort-by :module results)]
        (if-let [v (:ok latest)]
          (if (= v current)
            (log-ok (str module " " current))
            (println (str "  UPDATE: " module " " current " → " v)))
          (println (str "  " module " " current " " (:err latest)))))

      (println)

      ;; Sanity check: every module pulled from MODULE.bazel must have a
      ;; corresponding entry in versions.cue. A module in MODULE.bazel but
      ;; not in versions.cue means gen would not propagate the bump anywhere
      ;; (and patch-cue-version would silently no-op).
      (let [missing (->> results
                         (filter #(:ok (:latest %)))
                         (remove #(get versions (:module %)))
                         (map :module))]
        (when (seq missing)
          (log-err (str "BUG: bazel_dep(s) " (str/join ", " missing)
                        " present in MODULE.bazel but missing from versions.cue"))
          (exit 1)))

      ;; Patch versions.cue
      (let [patched (reduce
                      (fn [content {:keys [module current latest]}]
                        (if-let [v (:ok latest)]
                          (if (= v current)
                            content
                            (do
                              (reset! changed true)
                              (patch-cue-version content module v)))
                          content))
                      original
                      results)]
        (if @changed
          (do
            (spit cue-path patched)
            (sh! "chmod" "644" cue-path)
            (log-ok "updated schema/versions.cue with new module versions")
            (println)
            (log-ok "hatching bzlmod upgrade to equilibrium...")
            (defn-bin! "hatch" "bzlmodupgrade"))
          (log-ok "all Bazel modules are at latest versions"))))))


;; ---------------------------------------------------------------------------
;; Subcommand: mise
;; ---------------------------------------------------------------------------

(defn read-mise-toml-versions
  "Parse the mise [tools] section and return {tool-name version-string}.
   Handles both simple (tool = \"ver\") and complex (tool = { version = \"ver\" }) forms.
   Reads root/.config/mise/config.toml -- the global mise config, symlinked from
   ~/.config/mise/config.toml -- since m/mise.toml has no [tools] section."
  []
  (let [lines   (str/split-lines (slurp "root/.config/mise/config.toml"))
        in-tools (atom false)
        result   (atom {})]
    (doseq [line lines]
      (cond
        (re-matches #"\[tools\]" line)
        (reset! in-tools true)

        (and @in-tools (re-matches #"\[.+\]" line))
        (reset! in-tools false)

        (and @in-tools (not (str/starts-with? (str/trim line) "#"))
             (not (str/blank? line)))
        (when-let [m (or
                       ;; Complex: tool = { version = "ver", ... }
                       (re-matches #"\"?([^\"\s=]+)\"?\s*=\s*\{\s*version\s*=\s*\"([^\"]+)\".*" line)
                       ;; Simple: tool = "ver"
                       (re-matches #"\"?([^\"\s=]+)\"?\s*=\s*\"([^\"]+)\"" line))]
          (swap! result assoc (nth m 1) (nth m 2)))))
    @result))


;; Mapping from mise tool IDs to CUE keys in versions.cue, derived by parsing
;; kernel/schema/mise.cue. Each entry in root_mise_tools pairs an `id: "X"`
;; with a `version: versions.Y.version` (or `versions["Y"].version`); we read
;; those pairs out as the source of truth.
;;
;; History: this used to be a hand-maintained table, which silently dropped
;; any tool whose mise ID didn't have an explicit mapping (e.g. river,
;; buildkite_cli, pitchfork). The drop fell out as "no version changes from
;; mise upgrade", and gen then regenerated config.toml from the stale
;; versions.cue, masking the bump entirely.
(defn build-mise-id->cue-key
  "Parse kernel/schema/mise.cue and return {mise-id cue-key}."
  []
  (let [content (slurp "kernel/schema/mise.cue")
        ;; Match `id: "X" ... version: versions.Y.version`
        ;; or    `id: "X" ... version: versions["Y"].version`
        ;; (?s) = dotall so .*? crosses newlines within a single block.
        pat     #"(?s)id:\s*\"([^\"]+)\".*?version:\s*versions(?:\.([a-zA-Z_][a-zA-Z0-9_]*)|\[\"([^\"]+)\"\])\.version"]
    (->> (re-seq pat content)
         (map (fn [[_ mise-id dot-key bracket-key]]
                [mise-id (or dot-key bracket-key)]))
         (into {}))))


(def mise-id->cue-key (delay (build-mise-id->cue-key)))


(defn resolve-cue-key
  "Map a mise tool ID to its CUE key in versions.cue.
   Returns the mapped key, or the original ID if no mapping exists."
  [tool-id]
  (get @mise-id->cue-key tool-id tool-id))


(defn upgrade-mise!
  "Run mise upgrade --bump, then sync constrained versions back."
  []
  (println "=== upgrade mise ===")
  (println)

  ;; Step 1: bump all mise tools (excluding bzlmod-constrained and special-handling ones)
  ;; uv, python, node, java, crane, regctl versions are pinned to what their
  ;; Bazel modules support.  Upgrading them here would just get rolled back
  ;; by sync-module-versions.
  ;; tailscale requires special handling (chart + image mirror coordination).
  ;; pnpm must sync with package.json packageManager field (otherwise pnpm
  ;; reinstalls endlessly); bump it manually in schema/versions.cue +
  ;; package.json together, then run `mise run gen`.
  (log-ok "running mise upgrade --bump (excluding bzlmod-pinned: uv, python, node, java, crane, regctl; special: tailscale, pnpm)")
  (sh!! "mise" "upgrade" "--bump"
        "--exclude" "uv" "--exclude" "python" "--exclude" "node"
        "--exclude" "java"
        "--exclude" "github:google/go-containerregistry"
        "--exclude" "github:regclient/regclient"
        "--exclude" "aqua:tailscale/tailscale"
        "--exclude" "pnpm")
  (println)

  ;; Step 2: read bumped versions from mise.toml back into versions.cue
  ;; mise upgrade --bump edits mise.toml directly, but versions.cue is our
  ;; source of truth. We need to propagate the bumped versions back.
  (log-ok "reading bumped versions from mise.toml")
  (let [bumped-versions (read-mise-toml-versions)
        cue-path        "kernel/schema/versions.cue"
        original        (slurp cue-path)
        versions-json   (run-tool-quiet "cue" "eval" "-e" "versions" "--out" "json"
                                        "./kernel/schema")
        cue-versions    (parse-json versions-json)
        changed         (atom false)]

    (let [patched (reduce
                    (fn [content [tool-id bumped-ver]]
                      (let [cue-key (resolve-cue-key tool-id)]
                        (if (get cue-versions cue-key)
                          (let [current (get-in cue-versions [cue-key "version"])]
                            (if (= bumped-ver current)
                              content
                              (do
                                (println (str "  " cue-key " " current " → " bumped-ver))
                                (reset! changed true)
                                (patch-cue-version content cue-key bumped-ver))))
                          ;; Tool exists in mise.toml but not in versions.cue.
                          ;; If kernel/schema/mise.cue lists it, the cue-key
                          ;; mapping is broken; if not, the tool is managed
                          ;; outside CUE -- skip with a warning.
                          (let [from-schema? (contains? @mise-id->cue-key tool-id)]
                            (if from-schema?
                              (do
                                (log-err (str "BUG: tool " tool-id " maps to cue-key " cue-key
                                              " but versions." cue-key " is not present in versions.cue"))
                                (exit 1))
                              (do
                                (log-ok (str "skipping " tool-id " (not in kernel/schema/mise.cue)"))
                                content))))))
                    original
                    bumped-versions)]
      (when @changed
        (spit cue-path patched)
        (sh! "chmod" "644" cue-path)
        (log-ok "updated schema/versions.cue with bumped versions"))
      (when-not @changed
        (log-ok "no version changes from mise upgrade"))))

  (println)

  ;; Step 3: sync constrained tool versions back to module-supported values
  (log-ok "syncing constrained tool versions to module-supported values")
  (sh!! ".mise/tasks/sync-module-versions.clj")
  (println)

  ;; Step 4: hatch to equilibrium and validate
  (log-ok "hatching mise upgrade to equilibrium...")
  (defn-bin! "hatch")
  (println)
  (log-ok "validating...")
  (sh!! "mise" "run" "gen")
  (println)
  (log-ok "mise upgrade complete"))


