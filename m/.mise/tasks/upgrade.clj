(defn- direct-version
  "Return the version string for a direct require in go.mod, or nil if not present
   as a direct dep. Matches '\\t<path> v<version>' lines outside the indirect set."
  [mod-content path]
  (let [pat (re-pattern (str "(?m)^\\t" (java.util.regex.Pattern/quote path) "\\s+(v\\S+)\\s*$"))]
    (when-let [m (re-find pat mod-content)]
      (second m))))


(defn- parse-cr-k8s-pins
  "Parse sigs.k8s.io/controller-runtime's go.mod from GOMODCACHE and return a
   map of {k8s.io/<name> version} for every k8s.io/* require it lists (direct
   or indirect). The pseudo-version of kube-openapi is included."
  [cr-version]
  (let [gomodcache (sh! "go" "env" "GOMODCACHE")
        cr-mod    (str gomodcache "/sigs.k8s.io/controller-runtime@" cr-version "/go.mod")]
    (when (fs/exists? cr-mod)
      (->> (slurp cr-mod)
           (re-seq #"(?m)^\s*(k8s\.io/[^\s]+)\s+(v\S+)")
           (map (fn [[_ p v]] [p v]))
           (into {})))))


(defn- pseudo-version?
  "True if v is a Go pseudo-version (vX.Y.Z-...YYYYMMDDHHMMSS-<12-hex>).
   Pseudo-versions are timestamp-ordered branch snapshots, not tagged releases."
  [v]
  (boolean (re-find #"\d{14}-[0-9a-f]{12}$" v)))


(defn clamp-k8s-to-controller-runtime!
  "If sigs.k8s.io/controller-runtime is a direct dep, downgrade any k8s.io/*
   directs that now exceed the versions controller-runtime requires. No-op
   when controller-runtime is not a direct dep, or when its pins already match.

   Skips pseudo-version pins (e.g. kube-openapi, k8s.io/utils): those are
   timestamp-ordered continuous-branch snapshots, not breaking semver releases,
   so MVS picks the higher one safely. Clamping them would actively undo every
   `go get -u` bump of those modules."
  []
  (let [mod-content (slurp "go.mod")
        cr-version  (direct-version mod-content "sigs.k8s.io/controller-runtime")]
    (when cr-version
      (let [cr-pins (parse-cr-k8s-pins cr-version)
            ;; Only clamp k8s.io/* that we list as a direct dep.
            pins    (->> cr-pins
                         (filter (fn [[p _]] (direct-version mod-content p)))
                         (filter (fn [[p v]] (not= v (direct-version mod-content p))))
                         (remove (fn [[_ v]] (pseudo-version? v))))]
        (when (seq pins)
          (log-ok (str "clamping k8s.io/* to controller-runtime " cr-version " pins"))
          (apply sh!! "go" "get"
                 (for [[p v] pins] (str p "@" v)))
          (println))))))


(defn upgrade-go!
  "Upgrade Go module dependencies."
  []
  (println "=== upgrade go ===")
  (println)

  ;; Step 1: upgrade all direct dependencies listed in go.mod
  ;;
  ;; Run `go get -u` per-package rather than passing the whole list at once.
  ;; A combined `go get -u a b c` makes every listed module's @upgrade target
  ;; a hard constraint, so when one direct (e.g. k8s.io/api) transitively
  ;; pins a pseudo-version of another listed direct (e.g. protobuf) that is
  ;; newer than that direct's latest tag, Go errors out instead of letting
  ;; MVS pick the higher version. Per-package upgrades let each transitive
  ;; bump flow through naturally.
  ;;
  ;; Note: `-u` is intentionally aggressive about transitive deps. Some
  ;; tooling-only transitives (e.g. counterfeiter, pulled in by
  ;; controller-idioms's tests) get bumped here and dropped by `go mod tidy`
  ;; in step 3, so you'll see "go: upgraded X v.. => v.." for X that never
  ;; sticks. We tried `go get <pkg>@latest` (no -u) to silence that, but it
  ;; caused MVS to thrash through pre-release versions (e.g. v0.36.0-alpha.2)
  ;; on the way to a stable resolution -- worse noise. Keep `-u` and live
  ;; with the cosmetic re-bump.
  (log-ok "upgrading Go module dependencies")
  (let [mod-content (slurp "go.mod")
        ;; Extract direct (non-indirect) require paths
        directs (->> (re-seq #"(?m)^\t([^\s]+)\s+v[^\s]+\s*$" mod-content)
                     (map second))]
    (doseq [pkg directs]
      (sh!! "go" "get" "-u" pkg)))
  (println)

  ;; Step 2: clamp k8s.io/* to versions the upgraded controller-runtime supports.
  ;;
  ;; sigs.k8s.io/controller-runtime tags lag the k8s.io release cycle: a
  ;; freshly tagged k8s.io v0.N.0 is typically incompatible with the latest
  ;; controller-runtime, which still requires v0.(N-1).x and uses APIs (e.g.
  ;; ResourceEventHandlerRegistration) the new k8s.io has changed. Step 1
  ;; pushes everything to its individual @upgrade head, leaving k8s.io ahead
  ;; of controller-runtime's pinned set. Re-pin k8s.io/* directs to the
  ;; versions controller-runtime requires so the build remains consistent.
  (clamp-k8s-to-controller-runtime!)

  ;; Step 3: hatch to equilibrium (tidy + gen + validate)
  (log-ok "hatching go upgrade to equilibrium...")
  (defn-bin! "hatch" "goupgrade")
  (println)

  (log-ok "go upgrade complete"))


