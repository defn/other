// Package awstofu generates Tofu CR app bricks for AWS accounts and orgs.
//
// For every entry in catalog.aws_tofu_apps the package reads the
// corresponding infra/org/<org>[/<account>]/main.tf and stamps
// app/aws-{acc,org}-<key>/gen-app.cue with the .tf content embedded
// as a CUE raw multi-line string. BUILD.bazel for these apps is
// produced by the standard app generator since they are registered
// as kind: "raw" in catalog.apps.
package awstofu

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"cuelang.org/go/cue"
	"github.com/defn/other/m/tenant/library/go/lib/gen"
)

type entry struct {
	key      string // catalog key, e.g. "acc-jianghu-ops"
	org      string
	account  string // empty for org-level modules
	appDir   string // workspace-relative, e.g. tenant/<owner>/app/aws-acc-<org>-<account>
	srcPath  string // workspace-relative, e.g. tenant/<owner>/infra/org/<org>/<account>/main.tf
	crName   string // Tofu CR name, e.g. "acc-jianghu-ops"
	cmName   string // backing ConfigMap name, e.g. "tf-acc-jianghu-ops"
	stateKey string // S3 backend key, e.g. "stacks/acc-jianghu-ops/terraform.tfstate"
}

// Run stamps gen-app.cue for every entry in catalog.aws_tofu_apps.
func Run(ctx *gen.Context) error {
	// Tenant-rooted path bases: read the catalog's default_tenant
	// once. A fork swaps tenant via catalog.cue without code change.
	// See AIDR-00071.
	tenant := ctx.DefaultTenant()
	appBase := "tenant/" + tenant + "/app"
	infraOrgBase := "tenant/" + tenant + "/infra/org"
	// Master AWS profile lives in the tenant overlay's auth.tofu;
	// AIDR-00101 decoupled the embedded backend "profile" string from
	// a hardcoded master profile so a fork retargets via catalog only.
	authTofu, _ := gen.DecodeString(ctx.CatalogQuery("auth"), "tofu")

	apps := ctx.CatalogQuery("aws_tofu_apps")
	var entries []entry
	if err := gen.IterMap(apps, func(key string, v cue.Value) error {
		e := entry{key: gen.CueFieldKey(key)}
		var err error
		e.org, err = gen.DecodeString(v, "org")
		if err != nil {
			return fmt.Errorf("aws_tofu_apps[%q]: org: %w", e.key, err)
		}
		e.account = gen.DecodeStringOr(v, "account", "")
		if e.account != "" {
			e.appDir = appBase + "/aws-acc-" + e.org + "-" + e.account
			e.srcPath = infraOrgBase + "/" + e.org + "/" + e.account + "/main.tf"
			e.crName = "acc-" + e.org + "-" + e.account
		} else {
			e.appDir = appBase + "/aws-org-" + e.org
			e.srcPath = infraOrgBase + "/" + e.org + "/main.tf"
			e.crName = "org-" + e.org
		}
		e.cmName = "tf-" + e.crName
		e.stateKey = "stacks/" + e.crName + "/terraform.tfstate"
		entries = append(entries, e)
		return nil
	}); err != nil {
		return fmt.Errorf("iterate aws_tofu_apps: %w", err)
	}

	sort.Slice(entries, func(i, j int) bool { return entries[i].key < entries[j].key })

	for _, e := range entries {
		if err := stamp(ctx, e, authTofu); err != nil {
			return fmt.Errorf("stamp %s: %w", e.key, err)
		}
		ctx.LogOK("generated " + e.appDir + "/gen-app.cue")
	}
	return nil
}

func stamp(ctx *gen.Context, e entry, authTofu string) error {
	srcAbs := filepath.Join(ctx.WorkDir, e.srcPath)
	raw, err := os.ReadFile(srcAbs)
	if err != nil {
		return fmt.Errorf("read %s: %w", e.srcPath, err)
	}
	// CUE raw multi-line string strips leading whitespace matching the
	// closing #"""# delimiter (one tab here). Tab-prefix every line of
	// the .tf so the embedded data round-trips byte-for-byte.
	content := strings.TrimRight(string(raw), "\n")
	lines := strings.Split(content, "\n")
	for i, l := range lines {
		lines[i] = "\t" + l
	}
	indented := strings.Join(lines, "\n")

	body := fmt.Sprintf(`@experiment(aliasv2,explicitopen,shortcircuit,try)

// gen-app.cue -- Tofu CR brick for %s.
// Generated by defn gen from %s. DO NOT EDIT.
package app

_main_tf: #"""
%s
	"""#

_backend_hcl: """
	terraform {
	  backend "s3" {
	    bucket       = "dfn-defn-terraform-state"
	    key          = "%s"
	    region       = "us-east-1"
	    profile      = "%s"
	    encrypt      = true
	    use_lockfile = true
	  }
	}
	"""

objects: ConfigMap: "%s": {
	apiVersion: "v1"
	kind:       "ConfigMap"
	metadata: {
		name:      "%s"
		namespace: "terraform-system"
	}
	data: "main.tf": _main_tf
}

objects: Tofu: "%s": {
	apiVersion: "tf.defn.dev/v1beta1"
	kind:       "Tofu"
	metadata: {
		name:      "%s"
		namespace: "terraform-system"
	}
	spec: {
		// Runner image is resolved from the operator fork's
		// TerraformTaskImageRepoDefault default
		// (host.k3d.internal:5000/devcontainer/dev). Leaving images
		// unset keeps the rendered manifest free of image refs so
		// check-images doesn't need a mirror_images entry for the
		// locally-built devcontainer.
		terraformVersion: "edge"
		terraformModule: configMapSelector: name: "%s"
		backend:           _backend_hcl
		requireApproval:   true
		keepCompletedPods: true
		// AWS auth: not specified here. Per-tofu role association
		// will provide credentials to the runner pod (e.g. via
		// credentials.aws.irsa or a per-CR ServiceAccount). The
		// generated terraform config still references the master
		// profile (catalog.auth.tofu) via assume_role; the actual
		// credential source is supplied at deploy time.
	}
}
`, e.key, e.srcPath, indented, e.stateKey, authTofu, e.cmName, e.cmName, e.crName, e.crName, e.cmName)

	outPath := filepath.Join(ctx.WorkDir, e.appDir, "gen-app.cue")
	if err := os.MkdirAll(filepath.Dir(outPath), 0o755); err != nil {
		return err
	}
	if _, err := gen.WriteIfChanged(outPath, []byte(body), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", outPath, err)
	}
	// Run cue fmt so the file matches the buildifier-style fmt_test
	// in BUILD.bazel.
	relPath := filepath.Join(e.appDir, "gen-app.cue")
	return ctx.CueFmt(relPath)
}
