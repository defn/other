// Package awsconfig generates AWS profile config files from
// catalog.aws_orgs + aws_accounts + auth.tofu + image_bootstrap:
//
//   - root/.aws/config: one [profile] section per AWS account in
//     catalog.aws_accounts, with one chained [profile <org>-via-defn]
//     per org-management account whose source_profile is auth.tofu.
//
//   - kernel/image/packer/coder/mise.toml: AWS_PROFILE + AWS_REGION
//     env overrides for packer builds of the coder AMI. AWS_PROFILE
//     is auth.tofu (AIDR-00101); AWS_REGION is image_builder.region
//     when declared, defaultPackerRegion otherwise (AIDR-00106).
//
//   - kernel/image/packer/coder/install-ec2.sh: EC2 bootstrap script
//     for the coder AMI; the s5cmd s3 URL is templated from
//     catalog.image_bootstrap.zfs_docker.{bucket,key} so a fork
//     retargets the ZFS docker dataset source via catalog only
//     (AIDR-00104). Skipped when image_bootstrap is absent (e.g. the
//     boot tenant has no packer AMI).
//
// Replaces the pre-Stage-6 cue-export genrule that lived in the
// tenant's aws/ BUILD.bazel:aws_config_gen and was blocked from
// the catalog overlay (cue export bypassed gen.Context's tenant
// catalog merge). This Go port queries the merged catalog directly via
// ctx.CatalogQuery, so aws.cue can live in tenant/<owner>/catalog/.
package awsconfig

import (
	"fmt"
	"path/filepath"
	"sort"
	"strings"

	"cuelang.org/go/cue"
	"github.com/defn/other/m/tenant/library/go/lib/gen"
)

// defaultPackerRegion is the fallback AWS region used to template the
// coder packer mise.toml's AWS_REGION env when a tenant declines to
// declare image_builder.region. Per AIDR-00106 Q1 (option b): keep the
// kernel-resident region literal in exactly one place.
const defaultPackerRegion = "us-west-2"

// Run reads aws_orgs + aws_accounts + aws_state from the merged
// catalog and writes root/.aws/config (INI). Skips accounts with
// empty id (placeholders).
func Run(ctx *gen.Context) error {
	orgs := ctx.CatalogQuery("aws_orgs")
	accounts := ctx.CatalogQuery("aws_accounts")
	state := ctx.CatalogQuery("aws_state")
	// Master AWS profile lives in the tenant overlay's auth.tofu;
	// AIDR-00101 decoupled this from a hardcoded master profile so a
	// fork retargets the chained-SSO source_profile via catalog only.
	authTofu, _ := gen.DecodeString(ctx.CatalogQuery("auth"), "tofu")

	stateRegion, _ := gen.DecodeString(state, "region")

	type acct struct {
		profile string
		id      string
		org     string
		ssoURL  string
		ssoReg  string
		isOrg   bool
	}
	var rows []acct
	if err := gen.IterMap(accounts, func(profileKey string, v cue.Value) error {
		id, _ := gen.DecodeString(v, "id")
		if id == "" {
			return nil
		}
		profile := gen.CueFieldKey(profileKey)
		name, _ := gen.DecodeString(v, "name")
		org, _ := gen.DecodeString(v, "org")
		orgEntry := orgs.LookupPath(cue.ParsePath(fmt.Sprintf("%q", org)))
		ssoURL, _ := gen.DecodeString(orgEntry, "sso_url")
		ssoReg, _ := gen.DecodeString(orgEntry, "sso_region")
		rows = append(rows, acct{
			profile: profile, id: id, org: org,
			ssoURL: ssoURL, ssoReg: ssoReg,
			isOrg: name == "org",
		})
		return nil
	}); err != nil {
		return fmt.Errorf("iterate aws_accounts: %w", err)
	}
	sort.Slice(rows, func(i, j int) bool { return rows[i].profile < rows[j].profile })

	const role = "Administrator"
	var b strings.Builder
	b.WriteString("# Generated by defn gen from catalog/aws.cue -- DO NOT EDIT.\n")
	b.WriteString("# Run: mise run gen\n")
	for _, r := range rows {
		fmt.Fprintf(&b, "[profile %s]\nsso_account_id = %s\nsso_role_name = %s\nsso_start_url = %s\nsso_region = %s\n\n",
			r.profile, r.id, role, r.ssoURL, r.ssoReg)
	}
	// Per-org chained terraform-bootstrap profiles (one per org-management
	// account) used as source_profile for account bootstrap. Mirrors the
	// pre-Go _via_defn_profiles section.
	for _, r := range rows {
		if !r.isOrg {
			continue
		}
		fmt.Fprintf(&b, "[profile %s-via-defn]\nrole_arn = arn:aws:iam::%s:role/%s-ops-terraform\nsource_profile = %s\nregion = %s\n\n",
			r.org, r.id, r.org, authTofu, stateRegion)
	}

	out := filepath.Join(ctx.WorkDir, "root", ".aws", "config")
	if _, err := gen.WriteIfChanged(out, []byte(b.String()), 0o644); err != nil {
		return fmt.Errorf("write root/.aws/config: %w", err)
	}
	ctx.LogOK("generated root/.aws/config")

	// Packer AMI build region: tenant-declared via image_builder.region,
	// falling back to defaultPackerRegion when the optional slot is
	// absent. Inner-path Exists() is the absent-field idiom for optional
	// CUE fields (per AIDR-00105 code-major #1 / mirrored from the
	// image_bootstrap probe below). Per AIDR-00106.
	region := defaultPackerRegion
	imgBuilder := ctx.CatalogQuery("image_builder")
	if imgBuilder.LookupPath(cue.ParsePath("region")).Exists() {
		r, err := gen.DecodeString(imgBuilder, "region")
		if err != nil {
			return fmt.Errorf("decode image_builder.region: %w", err)
		}
		region = r
	}
	packerMise := fmt.Sprintf("[env]\nAWS_PROFILE = %q\nAWS_REGION = %q\n", authTofu, region)
	packerOut := filepath.Join(ctx.WorkDir, "kernel", "image", "packer", "coder", "mise.toml")
	if _, err := gen.WriteIfChanged(packerOut, []byte(packerMise), 0o644); err != nil {
		return fmt.Errorf("write kernel/image/packer/coder/mise.toml: %w", err)
	}
	ctx.LogOK("generated kernel/image/packer/coder/mise.toml")

	// install-ec2.sh -- s5cmd s3 source templated from
	// image_bootstrap.zfs_docker.{bucket,key}. Optional: tenants without
	// a coder packer AMI omit the field and we skip the stamp.
	//
	// Probe the inner zfs_docker path rather than the outer image_bootstrap
	// slot because optional schema fields keep the outer slot in the
	// struct shape -- LookupPath against the concrete catalog reports
	// Exists()==true on a non-concrete value and the subsequent
	// DecodeString calls fail. Inner-path Exists() is the absent-field
	// idiom for optional CUE fields. Per AIDR-00105 code-major #1.
	imgBoot := ctx.CatalogQuery("image_bootstrap")
	if imgBoot.LookupPath(cue.ParsePath("zfs_docker")).Exists() {
		bucket, err := gen.DecodeString(imgBoot, "zfs_docker.bucket")
		if err != nil {
			return fmt.Errorf("decode image_bootstrap.zfs_docker.bucket: %w", err)
		}
		key, err := gen.DecodeString(imgBoot, "zfs_docker.key")
		if err != nil {
			return fmt.Errorf("decode image_bootstrap.zfs_docker.key: %w", err)
		}
		script := installEC2Template(bucket, key)
		installOut := filepath.Join(ctx.WorkDir, "kernel", "image", "packer", "coder", "install-ec2.sh")
		// 0o755 matches the manifest schema (#PackerImage requires
		// `*.sh` to be _#exe / mode 100755). Sibling install-packer.sh
		// follows the same convention.
		if _, err := gen.WriteIfChanged(installOut, []byte(script), 0o755); err != nil {
			return fmt.Errorf("write kernel/image/packer/coder/install-ec2.sh: %w", err)
		}
		ctx.LogOK("generated kernel/image/packer/coder/install-ec2.sh")
	}
	return nil
}
