// Package cuetree generates manifest/gen-manifest.cue from git ls-files.
package cuetree

import (
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/defn/other/m/tenant/library/go/lib/gen"
)

type fileInfo struct {
	fileType string // "file" or "symlink"
	mode     string
	target   string // symlink target, empty for files
}

type tree struct {
	files map[string]fileInfo
	dirs  map[string]*tree
}

func newTree() *tree {
	return &tree{files: map[string]fileInfo{}, dirs: map[string]*tree{}}
}

// Run generates manifest/gen-manifest.cue from git ls-files.
func Run(ctx *gen.Context) error {
	info, err := gitFileInfo(ctx)
	if err != nil {
		return err
	}

	t := buildTree(info)

	var sb strings.Builder
	sb.WriteString("// Generated by defn gen from git ls-files.\n")
	sb.WriteString("// DO NOT EDIT -- run `mise run gen` to regenerate.\n\n")
	sb.WriteString("@experiment(aliasv2,explicitopen,shortcircuit,try)\n\n")
	sb.WriteString("package manifest\n\n")
	sb.WriteString("repo: {\n")
	sb.WriteString("\ttype: \"dir\"\n")
	emitCUE(&sb, t, 1)
	sb.WriteString("}\n")

	outPath := filepath.Join(ctx.WorkDir, "var/gen-manifest.cue")
	if _, err := gen.WriteIfChanged(outPath, []byte(sb.String()), 0o644); err != nil {
		return fmt.Errorf("write gen-manifest.cue: %w", err)
	}
	return nil
}

func gitFileInfo(ctx *gen.Context) (map[string]fileInfo, error) {
	out, err := ctx.Sh("git", "ls-files", "-s")
	if err != nil {
		return nil, fmt.Errorf("git ls-files: %w", err)
	}
	result := map[string]fileInfo{}
	for _, line := range strings.Split(out, "\n") {
		if line == "" {
			continue
		}
		parts := strings.SplitN(line, " ", 3)
		if len(parts) < 3 {
			continue
		}
		mode := parts[0]
		rest := strings.SplitN(parts[2], "\t", 2)
		if len(rest) < 2 {
			continue
		}
		path := rest[1]
		isSymlink := mode == "120000"
		fi := fileInfo{mode: mode}
		if isSymlink {
			fi.fileType = "symlink"
			// Read symlink target from the file itself
			target, _ := os.ReadFile(filepath.Join(ctx.WorkDir, path))
			fi.target = strings.TrimSpace(string(target))
		} else {
			fi.fileType = "file"
		}
		result[path] = fi
	}
	return result, nil
}

func buildTree(info map[string]fileInfo) *tree {
	t := newTree()
	for path, fi := range info {
		parts := strings.Split(path, "/")
		insertIntoTree(t, parts, fi)
	}
	return t
}

func insertIntoTree(t *tree, parts []string, fi fileInfo) {
	if len(parts) == 1 {
		t.files[parts[0]] = fi
		return
	}
	dir := parts[0]
	sub, ok := t.dirs[dir]
	if !ok {
		sub = newTree()
		t.dirs[dir] = sub
	}
	insertIntoTree(sub, parts[1:], fi)
}

func emitCUE(sb *strings.Builder, t *tree, depth int) {
	pfx := strings.Repeat("\t", depth)

	if len(t.files) > 0 {
		fmt.Fprintf(sb, "%sfiles: {\n", pfx)
		var fnames []string
		for k := range t.files {
			fnames = append(fnames, k)
		}
		sort.Strings(fnames)
		for _, fname := range fnames {
			fi := t.files[fname]
			if fi.fileType == "symlink" {
				fmt.Fprintf(sb, "%s\t\"%s\": {type: \"symlink\", mode: \"%s\", target: \"%s\"}\n", pfx, fname, fi.mode, fi.target)
			} else {
				fmt.Fprintf(sb, "%s\t\"%s\": {type: \"file\", mode: \"%s\"}\n", pfx, fname, fi.mode)
			}
		}
		fmt.Fprintf(sb, "%s}\n", pfx)
	}

	if len(t.dirs) > 0 {
		fmt.Fprintf(sb, "%sdirs: {\n", pfx)
		var dnames []string
		for k := range t.dirs {
			dnames = append(dnames, k)
		}
		sort.Strings(dnames)
		for _, dname := range dnames {
			fmt.Fprintf(sb, "%s\t\"%s\": {\n", pfx, dname)
			fmt.Fprintf(sb, "%s\t\ttype: \"dir\"\n", pfx)
			emitCUE(sb, t.dirs[dname], depth+2)
			fmt.Fprintf(sb, "%s\t}\n", pfx)
		}
		fmt.Fprintf(sb, "%s}\n", pfx)
	}
}
