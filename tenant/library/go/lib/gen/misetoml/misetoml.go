// Package misetoml generates mise.toml and root/mise.toml from schema.
package misetoml

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"cuelang.org/go/cue"
	"github.com/defn/other/m/tenant/library/go/lib/gen"
)

// Run generates m/mise.toml (stdout capture equivalent) and root/mise.toml.
func Run(ctx *gen.Context) error {
	schema := ctx.SchemaQuery("")
	rootTools := schema.LookupPath(cue.ParsePath("root_mise_tools"))

	taskDirs, err := discoverTaskDirs(ctx.WorkDir)
	if err != nil {
		return fmt.Errorf("discover .mise/tasks dirs: %w", err)
	}

	// m/mise.toml (settings, env, task_config only -- tools are global)
	miseContent := emitMiseToml(taskDirs)
	misePath := filepath.Join(ctx.WorkDir, "mise.toml")
	if _, err := gen.WriteIfChanged(misePath, []byte(miseContent), 0o644); err != nil {
		return fmt.Errorf("write mise.toml: %w", err)
	}

	// root/.config/mise/config.toml (mise global config)
	rootContent := emitRootMiseToml(rootTools)
	rootPath := filepath.Join(ctx.WorkDir, "root/.config/mise/config.toml")
	if _, err := gen.WriteIfChanged(rootPath, []byte(rootContent), 0o644); err != nil {
		return fmt.Errorf("write root/.config/mise/config.toml: %w", err)
	}

	return nil
}

func emitMiseToml(taskDirs []string) string {
	var sb strings.Builder
	sb.WriteString("# Generated by defn gen from schema/versions.cue.\n")
	sb.WriteString("# DO NOT EDIT -- edit schema/versions.cue and run `mise run gen`.\n")
	sb.WriteString("\n[settings]\nexperimental = true\n")
	sb.WriteString("\n[env]\n# Add m/bin to PATH so bbs shebang is available for mise tasks and scripts\n_.path = [\"bin\"]\n")
	sb.WriteString("\n[task_config]\n# includes replaces default .mise/tasks discovery; list discovered from filesystem\n")
	sb.WriteString("includes = [\n")
	for _, d := range taskDirs {
		fmt.Fprintf(&sb, "  %q,\n", d)
	}
	sb.WriteString("]\n")
	return sb.String()
}

// discoverTaskDirs walks rootDir for every directory whose path ends in
// ".mise/tasks" (excluding vendored/external trees). The returned paths
// are workspace-relative, sorted, and stable across runs.
//
// "other" is skipped to match the .bazelignore rule -- it is a nested
// bootstrap-target tree (AIDR-00138) and its .mise/tasks must not be
// surfaced in the host's mise.toml.
func discoverTaskDirs(rootDir string) ([]string, error) {
	skipDirs := map[string]bool{
		"vendor": true, "v": true, "node_modules": true,
		"bazel-bin": true, "bazel-out": true, "bazel-testlogs": true,
		".git": true, "other": true,
	}
	var out []string
	err := filepath.WalkDir(rootDir, func(path string, d os.DirEntry, err error) error {
		if err != nil {
			return err
		}
		if !d.IsDir() {
			return nil
		}
		rel, _ := filepath.Rel(rootDir, path)
		if rel == "." {
			return nil
		}
		base := filepath.Base(rel)
		if skipDirs[base] || strings.HasPrefix(base, "bazel-") {
			return filepath.SkipDir
		}
		if strings.HasSuffix(rel, ".mise/tasks") {
			out = append(out, rel)
			return filepath.SkipDir
		}
		return nil
	})
	if err != nil {
		return nil, err
	}
	sort.Strings(out)
	return out, nil
}

func emitRootMiseToml(tools cue.Value) string {
	var sb strings.Builder
	sb.WriteString("# Generated by defn gen from schema/versions.cue.\n")
	sb.WriteString("# DO NOT EDIT -- edit schema/versions.cue and run `mise run gen`.\n")
	sb.WriteString("\n[settings]\nexperimental = true\n")
	sb.WriteString("\n[settings.python]\ncompile = false\n")
	sb.WriteString("\n[tools]\n")
	emitToolEntries(&sb, tools)
	return sb.String()
}

func emitToolEntries(sb *strings.Builder, tools cue.Value) {
	iter, err := tools.List()
	if err != nil {
		return
	}
	for iter.Next() {
		v := iter.Value()
		id, _ := gen.DecodeString(v, "id")
		version, _ := gen.DecodeString(v, "version")
		comment := gen.DecodeStringOr(v, "comment", "")
		group := gen.DecodeStringOr(v, "group", "")

		// Notes array
		var notes []string
		notesVal := v.LookupPath(cue.ParsePath("notes"))
		if notesIter, err := notesVal.List(); err == nil {
			for notesIter.Next() {
				s, _ := notesIter.Value().String()
				notes = append(notes, s)
			}
		}

		// Opts map
		opts := map[string]string{}
		optsVal := v.LookupPath(cue.ParsePath("opts"))
		if optsIter, err := optsVal.Fields(); err == nil {
			for optsIter.Next() {
				k := gen.CueFieldKey(optsIter.Selector().String())
				s, _ := optsIter.Value().String()
				opts[k] = s
			}
		}

		if group != "" {
			sb.WriteString("\n")
			fmt.Fprintf(sb, "# %s\n", group)
		}
		if comment != "" {
			fmt.Fprintf(sb, "# %s\n", comment)
		}
		for _, n := range notes {
			fmt.Fprintf(sb, "# %s\n", n)
		}

		if len(opts) > 0 {
			// Complex entry
			var pairs []string
			pairs = append(pairs, fmt.Sprintf("version = \"%s\"", version))
			var keys []string
			for k := range opts {
				keys = append(keys, k)
			}
			sort.Strings(keys)
			for _, k := range keys {
				pairs = append(pairs, fmt.Sprintf("%s = \"%s\"", k, opts[k]))
			}
			fmt.Fprintf(sb, "%s = { %s }\n", tomlKey(id), strings.Join(pairs, ", "))
		} else {
			fmt.Fprintf(sb, "%s = \"%s\"\n", tomlKey(id), version)
		}
	}
}

var needsQuoting = regexp.MustCompile(`[^a-zA-Z0-9_-]`)

func tomlKey(s string) string {
	if needsQuoting.MatchString(s) {
		return `"` + s + `"`
	}
	return s
}
