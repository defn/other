// Package seed ensures chart_versions entries exist for all platform apps
// and gen-chart-digests.cue has entries for all chart_versions apps.
//
// Post-AIDR-00072 (kernel/tenant chart_versions decoupling): the
// chart_versions value layer lives in tenant/<t>/catalog/chart_versions.cue
// (one per tenant that owns clusters). seedChartVersions writes new
// entries into the appropriate tenant overlay based on each cluster's
// path, instead of stamping a Cartesian product into kernel/catalog/catalog.cue.
package seed

import (
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"sort"
	"strings"

	"cuelang.org/go/cue"
	"github.com/defn/other/m/tenant/library/go/lib/gen"
)

var digestRe = regexp.MustCompile(`chart_versions:\s*"?([a-z][a-z0-9-]*)"?:\s*cluster_digests:\s*"([a-z][a-z0-9-]*)"\s*:\s*build_digest:\s*"([^"]*)"`)

// versionEntryRe matches existing chart_versions entries in catalog.cue.
var versionEntryRe = regexp.MustCompile(`chart_versions:\s*\{[^}]*\}`)

// Run seeds chart_versions in catalog.cue for platform apps that lack entries,
// then seeds gen-chart-digests.cue with empty digests for new apps.
// Step 3 emits kernel/spec/tenant-deps.bzl listing tenant filegroup labels
// so kernel/spec/BUILD.bazel doesn't hardcode specific tenants (AIDR-00138 D5.3).
func Run(ctx *gen.Context) error {
	// Step 1: auto-seed chart_versions for platform apps missing entries.
	if err := seedChartVersions(ctx); err != nil {
		return err
	}

	// Step 2: seed gen-chart-digests.cue (reads updated chart_versions).
	if err := seedChartDigests(ctx); err != nil {
		return err
	}

	// Step 3: emit tenant-deps.bzl with per-tenant filegroup labels.
	return seedTenantDepsBzl(ctx)
}

// seedTenantDepsBzl walks tenant/<t>/ and writes kernel/spec/tenant-deps.bzl
// with three Starlark lists used by kernel/spec/BUILD.bazel tests:
//   - TENANT_CATALOG_FILES_LABELS: //tenant/<t>/catalog:catalog_files
//     (every tenant whose catalog/ has any .cue file)
//   - TENANT_AUTH_CUE_LABELS: //tenant/<t>/catalog:auth.cue
//     (every leaf tenant -- one with catalog/auth.cue)
//   - TENANT_SPEC_FILES_LABELS: //tenant/<t>/spec:spec_files
//     (every tenant with a spec/ dir)
//
// Generated by seed (phase 0) so it's on-disk before Bazel analysis
// reads kernel/spec/BUILD.bazel. Per AIDR-00138 D5.3.
func seedTenantDepsBzl(ctx *gen.Context) error {
	tenantsDir := filepath.Join(ctx.WorkDir, "tenant")
	entries, err := os.ReadDir(tenantsDir)
	if err != nil {
		if os.IsNotExist(err) {
			return nil
		}
		return fmt.Errorf("read tenant dir: %w", err)
	}
	var catalogLabels, authLabels, specLabels []string
	for _, e := range entries {
		if !e.IsDir() {
			continue
		}
		name := e.Name()
		catDir := filepath.Join(tenantsDir, name, "catalog")
		if hasCUEFiles(catDir) {
			catalogLabels = append(catalogLabels, fmt.Sprintf("//tenant/%s/catalog:catalog_files", name))
		}
		if _, err := os.Stat(filepath.Join(catDir, "auth.cue")); err == nil {
			authLabels = append(authLabels, fmt.Sprintf("//tenant/%s/catalog:auth.cue", name))
		}
		specDir := filepath.Join(tenantsDir, name, "spec")
		if hasCUEFiles(specDir) {
			specLabels = append(specLabels, fmt.Sprintf("//tenant/%s/spec:spec_files", name))
		}
	}
	sort.Strings(catalogLabels)
	sort.Strings(authLabels)
	sort.Strings(specLabels)

	var sb strings.Builder
	sb.WriteString("# Generated by `defn gen` (seed phase) -- do not hand-edit.\n")
	sb.WriteString("# Tenant filegroup labels consumed by kernel/spec/BUILD.bazel\n")
	sb.WriteString("# tests; AIDR-00138 D5.3 -- kernel BUILD has no hardcoded tenants.\n\n")
	writeBzlList(&sb, "TENANT_CATALOG_FILES_LABELS", catalogLabels)
	sb.WriteString("\n")
	writeBzlList(&sb, "TENANT_AUTH_CUE_LABELS", authLabels)
	sb.WriteString("\n")
	writeBzlList(&sb, "TENANT_SPEC_FILES_LABELS", specLabels)

	outPath := filepath.Join(ctx.WorkDir, "kernel/spec/tenant-deps.bzl")
	if _, err := gen.WriteIfChanged(outPath, []byte(sb.String()), 0o644); err != nil {
		return fmt.Errorf("write tenant-deps.bzl: %w", err)
	}
	return nil
}

func hasCUEFiles(dir string) bool {
	entries, err := os.ReadDir(dir)
	if err != nil {
		return false
	}
	for _, e := range entries {
		if !e.IsDir() && strings.HasSuffix(e.Name(), ".cue") {
			return true
		}
	}
	return false
}

func writeBzlList(sb *strings.Builder, name string, vals []string) {
	if len(vals) == 0 {
		fmt.Fprintf(sb, "%s = []\n", name)
		return
	}
	fmt.Fprintf(sb, "%s = [\n", name)
	for _, v := range vals {
		fmt.Fprintf(sb, "    %q,\n", v)
	}
	sb.WriteString("]\n")
}

// seedChartVersions adds chart_versions entries to the appropriate
// tenant catalog file for chart-managed apps that don't yet have an
// entry. The owner is derived from each cluster's path field
// (tenant/<owner>/k3d/<x>): defn-owned clusters get entries in
// tenant/defn/catalog/chart_versions.cue, boot-owned clusters in
// tenant/boot/catalog/chart_versions.cue, etc.
func seedChartVersions(ctx *gen.Context) error {
	// Collect all app names referenced by any platform
	platformApps := map[string]bool{}
	platforms := ctx.CatalogQuery("k8s_platforms")
	gen.IterMap(platforms, func(_ string, p cue.Value) error {
		gen.IterMap(p.LookupPath(cue.ParsePath("apps")), func(appKey string, _ cue.Value) error {
			platformApps[gen.CueFieldKey(appKey)] = true
			return nil
		})
		return nil
	})

	// Both kustomize and raw apps need chart_versions (both become helm charts)
	apps := ctx.CatalogQuery("apps")
	chartApps := map[string]bool{}
	gen.IterMap(apps, func(key string, _ cue.Value) error {
		name := gen.CueFieldKey(key)
		if platformApps[name] {
			chartApps[name] = true
		}
		return nil
	})

	// Group clusters by owning tenant (parsed from cluster.path).
	// Per-tenant cluster lists let seed write each missing (app, cluster)
	// pair into the correct tenant overlay file.
	clusterOwners := clustersByTenant(ctx)
	if len(clusterOwners) == 0 {
		return nil
	}

	// Track which (app, cluster) pairs already exist across the merged catalog.
	chartVersions := ctx.CatalogQuery("chart_versions")
	type pair struct{ app, cluster string }
	existing := map[pair]bool{}
	gen.IterMap(chartVersions, func(appKey string, appVal cue.Value) error {
		appName := gen.CueFieldKey(appKey)
		cd := appVal.LookupPath(cue.ParsePath("cluster_digests"))
		gen.IterMap(cd, func(clusterKey string, _ cue.Value) error {
			existing[pair{appName, gen.CueFieldKey(clusterKey)}] = true
			return nil
		})
		return nil
	})

	// For each tenant, find missing (app, cluster) pairs and append to its
	// chart_versions.cue overlay file.
	tenants := make([]string, 0, len(clusterOwners))
	for t := range clusterOwners {
		tenants = append(tenants, t)
	}
	sort.Strings(tenants)
	missingApps := make([]string, 0, len(chartApps))
	for app := range chartApps {
		missingApps = append(missingApps, app)
	}
	sort.Strings(missingApps)

	for _, tenant := range tenants {
		clusters := clusterOwners[tenant]
		var entries []string
		for _, app := range missingApps {
			for _, cn := range clusters {
				if !existing[pair{app, cn}] {
					entries = append(entries, fmt.Sprintf("\t%q: cluster_digests: %q: version: \"0.0.1\"\n", app, cn))
					ctx.LogOK(fmt.Sprintf("seeded chart_versions for %s on %s (tenant %s)", app, cn, tenant))
				}
			}
		}
		if len(entries) == 0 {
			continue
		}
		if err := appendChartVersionEntries(ctx, tenant, entries); err != nil {
			return err
		}
	}
	return nil
}

// clustersByTenant groups k3d_clusters by owning tenant, parsed from
// each cluster's path field (tenant/<owner>/k3d/<x>).
func clustersByTenant(ctx *gen.Context) map[string][]string {
	owners := map[string][]string{}
	clusters := ctx.CatalogQuery("k3d_clusters")
	gen.IterMap(clusters, func(_ string, v cue.Value) error {
		cn, _ := gen.DecodeString(v, "cluster_name")
		path, _ := gen.DecodeString(v, "path")
		// path looks like "tenant/<owner>/k3d/<x>"; pull <owner>.
		parts := strings.Split(path, "/")
		if len(parts) < 2 || parts[0] != "tenant" {
			return nil
		}
		owners[parts[1]] = append(owners[parts[1]], cn)
		return nil
	})
	for _, list := range owners {
		sort.Strings(list)
	}
	return owners
}

// appendChartVersionEntries inserts new chart_versions entries into the
// closing brace of the existing chart_versions: { ... } block in
// tenant/<tenant>/catalog/chart_versions.cue. If the file doesn't exist,
// it's created with a minimal scaffold.
func appendChartVersionEntries(ctx *gen.Context, tenant string, entries []string) error {
	relPath := filepath.Join("tenant", tenant, "catalog", "chart_versions.cue")
	absPath := filepath.Join(ctx.WorkDir, relPath)
	body := strings.Join(entries, "")

	content, err := os.ReadFile(absPath)
	if err != nil {
		// File doesn't exist -- scaffold a minimal one. Newly stamped
		// tenants land here on first hatch.
		scaffold := "@experiment(aliasv2,explicitopen,shortcircuit,try)\n\n" +
			"// chart_versions data for " + tenant + "'s clusters. See AIDR-00072.\n" +
			"package catalog\n\n" +
			"chart_versions: {\n" + body + "}\n"
		if _, werr := gen.WriteIfChanged(absPath, []byte(scaffold), 0o644); werr != nil {
			return fmt.Errorf("write %s: %w", relPath, werr)
		}
		return ctx.CueFmt(relPath)
	}

	// Insert before the closing } of the top-level chart_versions: { ... } block.
	src := string(content)
	idx := strings.Index(src, "chart_versions: {")
	if idx < 0 {
		return fmt.Errorf("%s missing chart_versions: { ... } block", relPath)
	}
	// Find matching closing brace by counting depth.
	depth := 0
	closeIdx := -1
	for i := idx; i < len(src); i++ {
		switch src[i] {
		case '{':
			depth++
		case '}':
			depth--
			if depth == 0 {
				closeIdx = i
			}
		}
		if closeIdx != -1 {
			break
		}
	}
	if closeIdx < 0 {
		return fmt.Errorf("%s chart_versions block has unbalanced braces", relPath)
	}
	updated := src[:closeIdx] + body + src[closeIdx:]
	if _, err := gen.WriteIfChanged(absPath, []byte(updated), 0o644); err != nil {
		return fmt.Errorf("write %s: %w", relPath, err)
	}
	return ctx.CueFmt(relPath)
}

func seedChartDigests(ctx *gen.Context) error {
	outPath := filepath.Join(ctx.WorkDir, "var/gen-chart-digests.cue")

	// Read existing per-cluster digests: app -> cluster -> digest
	existingDigests := map[string]map[string]string{}
	if data, err := os.ReadFile(outPath); err == nil {
		for _, m := range digestRe.FindAllStringSubmatch(string(data), -1) {
			app, cluster, digest := m[1], m[2], m[3]
			if existingDigests[app] == nil {
				existingDigests[app] = map[string]string{}
			}
			existingDigests[app][cluster] = digest
		}
	}

	// Iterate (app, cluster) pairs from the merged chart_versions catalog.
	// Each tenant overlay only enumerates its own clusters, so the rollup
	// stays free of cross-tenant references at the data layer (AIDR-00072).
	chartVersions := ctx.CatalogQuery("chart_versions")

	type pair struct{ app, cluster string }
	var pairs []pair
	gen.IterMap(chartVersions, func(appKey string, appVal cue.Value) error {
		appName := gen.CueFieldKey(appKey)
		cd := appVal.LookupPath(cue.ParsePath("cluster_digests"))
		gen.IterMap(cd, func(clusterKey string, clusterVal cue.Value) error {
			// Skip orphan pairs (build_digest set, version not). See
			// AIDR-00108 -- without this filter seed.go would carry
			// orphans forward into gen-chart-digests.cue forever.
			if !clusterVal.LookupPath(cue.ParsePath("version")).IsConcrete() {
				return nil
			}
			pairs = append(pairs, pair{appName, gen.CueFieldKey(clusterKey)})
			return nil
		})
		return nil
	})
	sort.Slice(pairs, func(i, j int) bool {
		if pairs[i].app != pairs[j].app {
			return pairs[i].app < pairs[j].app
		}
		return pairs[i].cluster < pairs[j].cluster
	})

	if len(pairs) == 0 {
		return nil
	}

	var sb strings.Builder
	sb.WriteString("@experiment(aliasv2,explicitopen,shortcircuit,try)\n\n")
	sb.WriteString("// gen-chart-digests.cue -- generated by defn gen from Bazel build output.\n")
	sb.WriteString("// DO NOT EDIT. Run: mise run gen\n")
	sb.WriteString("package catalog\n\n")
	for _, p := range pairs {
		digest := ""
		if cd, ok := existingDigests[p.app]; ok {
			digest = cd[p.cluster]
		}
		fmt.Fprintf(&sb, "chart_versions: \"%s\": cluster_digests: \"%s\": build_digest: \"%s\"\n", p.app, p.cluster, digest)
	}

	if _, err := ctx.WriteCUEFmtIfChanged("var/gen-chart-digests.cue", []byte(sb.String())); err != nil {
		return fmt.Errorf("write gen-chart-digests.cue: %w", err)
	}
	return nil
}
