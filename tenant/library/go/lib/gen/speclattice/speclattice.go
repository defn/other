// Package speclattice generates spec/gen-lattice.cue from git ls-files.
package speclattice

import (
	"context"
	"fmt"
	"path/filepath"
	"sort"
	"strings"

	"github.com/defn/other/m/tenant/library/go/lib/gen"
	"github.com/defn/other/m/tenant/library/go/lib/runner"
)

// FileInfo describes a single file in the git tree.
type FileInfo struct {
	FileType string
	Mode     string
	Target   string // symlink target
}

// Tree represents a directory of files and subdirectories.
type Tree struct {
	Files map[string]FileInfo
	Dirs  map[string]*Tree
}

// NewTree creates an empty Tree.
func NewTree() *Tree {
	return &Tree{Files: map[string]FileInfo{}, Dirs: map[string]*Tree{}}
}

// ToMap converts the Tree to a map[string]interface{} matching the CUE/JSON
// structure used by lattice.go (type, files, dirs).
func (t *Tree) ToMap() map[string]interface{} {
	m := map[string]interface{}{"type": "dir"}
	if len(t.Files) > 0 {
		files := map[string]interface{}{}
		for name, fi := range t.Files {
			entry := map[string]interface{}{
				"type": fi.FileType,
				"mode": fi.Mode,
			}
			if fi.Target != "" {
				entry["target"] = fi.Target
			}
			files[name] = entry
		}
		m["files"] = files
	}
	if len(t.Dirs) > 0 {
		dirs := map[string]interface{}{}
		for name, sub := range t.Dirs {
			dirs[name] = sub.ToMap()
		}
		m["dirs"] = dirs
	}
	return m
}

// Run generates spec/gen-lattice.cue from git ls-files and returns the tree.
func Run(ctx *gen.Context) (*Tree, error) {
	info, err := gitFileInfo(ctx)
	if err != nil {
		return nil, err
	}
	ctx.GitFileCount = len(info)

	t := buildTree(info)

	var sb strings.Builder
	sb.WriteString("// Generated by defn gen from git ls-files.\n")
	sb.WriteString("// DO NOT EDIT -- run `mise run gen` to regenerate.\n\n")
	sb.WriteString("@experiment(aliasv2,explicitopen,shortcircuit,try)\n\n")
	sb.WriteString("package spec\n\n")
	sb.WriteString("lattice: {\n")
	sb.WriteString("\ttree: {\n")
	sb.WriteString("\t\ttype: \"dir\"\n")
	emitCUE(&sb, t, 2)
	sb.WriteString("\t}\n")
	sb.WriteString("}\n")

	outPath := filepath.Join(ctx.WorkDir, "var/gen-lattice.cue")
	if _, err := gen.WriteIfChanged(outPath, []byte(sb.String()), 0o644); err != nil {
		return nil, fmt.Errorf("write gen-lattice.cue: %w", err)
	}
	return t, nil
}

func gitFileInfo(ctx *gen.Context) (map[string]FileInfo, error) {
	// Get repo root (spec-lattice covers entire repo, not just m/)
	root, err := runner.Output(context.Background(), runner.Opts{
		Args: []string{"git", "rev-parse", "--show-toplevel"},
		Dir:  ctx.WorkDir,
	})
	if err != nil {
		return nil, fmt.Errorf("git rev-parse: %w", err)
	}

	out, err := runner.Output(context.Background(), runner.Opts{
		Args: []string{"git", "-C", root, "ls-files", "-s"},
	})
	if err != nil {
		return nil, fmt.Errorf("git ls-files: %w", err)
	}

	result := map[string]FileInfo{}
	for _, line := range strings.Split(out, "\n") {
		if line == "" {
			continue
		}
		parts := strings.SplitN(line, " ", 3)
		if len(parts) < 3 {
			continue
		}
		mode := parts[0]
		hash := parts[1]
		rest := strings.SplitN(parts[2], "\t", 2)
		if len(rest) < 2 {
			continue
		}
		path := rest[1]
		isSymlink := mode == "120000"
		fi := FileInfo{Mode: mode}
		if isSymlink {
			fi.FileType = "symlink"
			// Read symlink target from git cat-file (works even if file doesn't exist on disk)
			if target, err := runner.Output(context.Background(), runner.Opts{
				Args: []string{"git", "-C", root, "cat-file", "-p", hash},
			}); err == nil {
				fi.Target = target
			}
		} else {
			fi.FileType = "file"
		}
		result[path] = fi
	}
	return result, nil
}

func buildTree(info map[string]FileInfo) *Tree {
	t := NewTree()
	for path, fi := range info {
		parts := strings.Split(path, "/")
		insertIntoTree(t, parts, fi)
	}
	return t
}

func insertIntoTree(t *Tree, parts []string, fi FileInfo) {
	if len(parts) == 1 {
		t.Files[parts[0]] = fi
		return
	}
	dir := parts[0]
	sub, ok := t.Dirs[dir]
	if !ok {
		sub = NewTree()
		t.Dirs[dir] = sub
	}
	insertIntoTree(sub, parts[1:], fi)
}

func emitCUE(sb *strings.Builder, t *Tree, depth int) {
	pfx := strings.Repeat("\t", depth)

	if len(t.Files) > 0 {
		fmt.Fprintf(sb, "%sfiles: {\n", pfx)
		var fnames []string
		for k := range t.Files {
			fnames = append(fnames, k)
		}
		sort.Strings(fnames)
		for _, fname := range fnames {
			fi := t.Files[fname]
			if fi.FileType == "symlink" {
				fmt.Fprintf(sb, "%s\t\"%s\": {type: \"symlink\", mode: \"%s\", target: \"%s\"}\n", pfx, fname, fi.Mode, fi.Target)
			} else {
				fmt.Fprintf(sb, "%s\t\"%s\": {type: \"file\", mode: \"%s\"}\n", pfx, fname, fi.Mode)
			}
		}
		fmt.Fprintf(sb, "%s}\n", pfx)
	}

	if len(t.Dirs) > 0 {
		fmt.Fprintf(sb, "%sdirs: {\n", pfx)
		var dnames []string
		for k := range t.Dirs {
			dnames = append(dnames, k)
		}
		sort.Strings(dnames)
		for _, dname := range dnames {
			fmt.Fprintf(sb, "%s\t\"%s\": {\n", pfx, dname)
			fmt.Fprintf(sb, "%s\t\ttype: \"dir\"\n", pfx)
			emitCUE(sb, t.Dirs[dname], depth+2)
			fmt.Fprintf(sb, "%s\t}\n", pfx)
		}
		fmt.Fprintf(sb, "%s}\n", pfx)
	}
}
